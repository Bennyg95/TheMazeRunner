
#include "tile.h" // includes the tile.h file

#include <iostream>

using namespace std;

// ##############################################################
Tile::Tile()
// ##############################################################

{
	tile = 'x'; // initializes tile to 'x'
}
// ##############################################################

// ##############################################################
bool Tile::isWall(char tile)
// ##############################################################
{
	this->tile = tile;
	if(tile == 'x')
		return true; // returns true if tile == 'x'
	return false;
}
// ##############################################################

// ##############################################################
bool Tile::isPath(char tile)
// ##############################################################

{
	this->tile = tile;
	if(tile == ' ')
		return true; // returns true if tile == ' ';'
	return false;
}
// ##############################################################

// ##############################################################
char Tile::setWall()
// ##############################################################
{
	tile = 'x'; // sets tile to 'x'
	return tile;
}
// ##############################################################

// ##############################################################
char Tile::setPath()
// ##############################################################
{
	this->tile = tile;
	tile = ' '; // sets tile to ' '
	return tile;
}
