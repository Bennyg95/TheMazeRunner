
#include "tile.h" // includes the tile.h file

#include <iostream>

using namespace std;

// ##############################################################
Tile::Tile()
// ##############################################################

{
	tile = 'x'; // initializes tile to 'x'
}
// ##############################################################

// ##############################################################
void Tile::setIsWall(char tile)
// ##############################################################
{
	this->tile = tile;
	if(tile == 'x')
		isWall = true; // returns true if tile == 'x'
	else
        isWall = false;
}
// ##############################################################

// ##############################################################
char Tile::setWall()
// ##############################################################
{
	tile = 'x'; // sets tile to 'x'
	setIsWall('x');
	return tile;
}
// ##############################################################

// ##############################################################
char Tile::setPath()
// ##############################################################
{
	tile = ' '; // sets tile to ' '
	setIsWall(' ');
	return tile;
}
// ##############################################################

// ##############################################################
void Tile::setIsStart(bool isStart)
// ##############################################################
{
    setPath();
	this->isStart = isStart;
}
// ##############################################################

// ##############################################################
void Tile::setIsEnd(bool isEnd)
// ##############################################################
{
    setPath();
	this->isEnd = isEnd;
}
// ##############################################################

// ##############################################################
void Tile::setOriginDirection(char originDirection)
// ##############################################################
{
	this->originDirection = originDirection;
}
// ##############################################################

void Tile::setIsKeyLoc(bool isKeyLoc)
{
    this->isKeyLoc = isKeyLoc;
}